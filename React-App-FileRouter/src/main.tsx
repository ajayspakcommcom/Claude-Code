import React from "react";
import ReactDOM from "react-dom/client";
import { RouterProvider, createRouter } from "@tanstack/react-router";
import { routeTree } from "./routeTree.gen"; // auto-generated by TanStack Router plugin

// CONCEPT: Context in loaders
// Define the shape of router-level context.
// Every loader can read from this via the `context` argument.
// __root.tsx uses createRootRouteWithContext<RouterContext>() to type it.
export type RouterContext = {
  theme: "light" | "dark";
};

const router = createRouter({
  routeTree,

  // CONCEPT: Context in loaders
  // Values here flow into every loader as the `context` argument.
  // e.g. loader: async ({ context }) => { context.theme }
  context: {
    theme: "light",
  } satisfies RouterContext,

  // CONCEPT: Scroll restoration
  // TanStack Router remembers scroll positions on navigation.
  // When you go back, you land where you were — no manual scrollTo needed.
  scrollRestoration: true,

  // CONCEPT: Pending component timing
  // If a loader finishes in < 500ms, the pendingComponent is never shown.
  // This prevents a distracting spinner flash on fast responses.
  // If loader takes > 500ms → pendingComponent appears.
  defaultPendingMs: 500,

  // Keep pendingComponent visible for at least 300ms even if loader resolves fast.
  // Prevents jarring instant-disappear flicker.
  defaultPendingMinMs: 300,
});

// TypeScript module augmentation — full type safety across the app
declare module "@tanstack/react-router" {
  interface Register {
    router: typeof router;
  }
}

ReactDOM.createRoot(document.getElementById("root")!).render(
  <React.StrictMode>
    <RouterProvider router={router} />
  </React.StrictMode>
);
